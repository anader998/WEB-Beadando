        <h2>Kedves Látogató</h2>
		
        <p>
		A digitális kompetenciák létjogosultsága mára megkérdőjelezhetetlenné vált. Az infokommunikációs (IKT) technológiák alapjaiban változtatták meg azt, hogy hogyan tartjuk a kapcsolatot egymással, mikor és hogyan keresünk információt és hogyan gondolkozunk az üzletről. A Neumann János Nonprofit Közhasznú Kft. a Nemzeti Fejlesztési Minisztérium háttérintézményeként azt a célt tűzte ki, hogy minden magyar polgár és vállalkozás a digitalizáció nyertesévé váljon. Hisszük, hogy a versenyképességhez - mind egyéni, mind pedig országos szinten – szorosan hozzátartozik, hogy ezen a területen is alkalmazzuk a 21. század által nyújtott számos lehetőséget, ezáltal is elősegítve a gazdasági fejlődést.
		</p>
		<p>
Munkánk során két kulcsfontosságú területre fókuszálunk: az állampolgárok digitális kompetenciáinak fejlesztésére, valamint innovatív digitális technológiákat fejlesztő induló vállalkozások (startupok) nemzetközi piacra segítésére.
		</p>
		<p>
A Digitális kompetenciák fejlesztéséhez kapcsolódó projektek irányítását a társaságunk által működtetett Digitális Jólét Koordinációs Központ (DJKK) látja el. A DJKK szakmai irányítása alatt áll az országos lefedettségű Digitális Jólét Program Hálózat, melynek elsődleges feladata annak biztosítása, hogy a digitális kompetenciák fejlesztésének segítségével minden magyar állampolgárnak módjában álljon kihasználni a digitális ökoszisztéma által nyújtott lehetőségeket. A Digitális Jólét Program Pontokkal rendelkező településeken élők a helyi Digitális Jólét Program Mentorok segítségével megismerhetik a digitális világhoz való csatlakozás előnyeit.
</p>
<p>
Hiszünk benne, hogy a magyar startupok által kidolgozott innovatív megoldások jelenthetik a megoldást a jövő nagy kihívásaira, ezért társaságunk a hazai, IKT fókuszú startup cégek nemzetközi piacra lépését is támogatja és koordinálja az INPUT Program keretében ( www.inputprogram.com). Az INPUT Program missziója, hogy a közép-magyarországi régión kívüli szárnyukat próbálgató ötletgazdáknak, és a fejlődés útján elindult startupoknak átfogó segítséget nyújtson és ezáltal ők is hozzájáruljanak a digitális fejlődéshez. Az INPUT területi koordinátor hálózatának segítségével az ötletgazdák, startupok felmérhetik hiányterületeiket, bővíthetik kapcsolati tőkéjüket és megtalálhatják a számukra legmegfelelőbb mentort, befektetőt, illetve részt vehetnek nemzetközi eseményeken, versenyeken.

Oldalainkon olyan információkat gyűjtöttünk össze, melyek segítik abban, hogy hasznos ismereteket szerezzen cégünkről és programjainkról.
</p>
